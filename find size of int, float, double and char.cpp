//C++ Program to Find Size of int, float, double and char
#include<iostream>
using namespace std;
int main(){
    cout<<"The size of the interger in compiler is: "<<sizeof(int)<<endl;
    cout<<"The size of the float in compiler is: "<<sizeof(float)<<endl;
    cout<<"The size of the double in compiler is: "<<sizeof(double)<<endl;
    cout<<"The size of the char in compiler is: "<<sizeof(char)<<endl;
    cout<<"The size of the long double in compiler is: "<<sizeof(long double)<<endl;
    return 0;
}